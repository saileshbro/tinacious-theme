/**
 * Emit a macOS Terminal.app setup script under dist/terminal/ from palette/tokens.json.
 * Run the output AppleScript via: osascript dist/terminal/setup-profile.applescript
 */
import { ansiPairs, loadPalette, writeDistFile } from './lib/load-palette.mjs';

function hexToAppleScript(hex) {
  const h = hex.replace('#', '');
  const r = Math.round((parseInt(h.slice(0, 2), 16) / 255) * 65535);
  const g = Math.round((parseInt(h.slice(2, 4), 16) / 255) * 65535);
  const b = Math.round((parseInt(h.slice(4, 6), 16) / 255) * 65535);
  return `{${r}, ${g}, ${b}}`;
}

const ANSI_PROP = {
  black: '|ANSI black color|',
  red: '|ANSI red color|',
  green: '|ANSI green color|',
  yellow: '|ANSI yellow color|',
  blue: '|ANSI blue color|',
  magenta: '|ANSI magenta color|',
  cyan: '|ANSI cyan color|',
  white: '|ANSI white color|',
};

const ANSI_BRIGHT_PROP = {
  black: '|ANSI bright black color|',
  red: '|ANSI bright red color|',
  green: '|ANSI bright green color|',
  yellow: '|ANSI bright yellow color|',
  blue: '|ANSI bright blue color|',
  magenta: '|ANSI bright magenta color|',
  cyan: '|ANSI bright cyan color|',
  white: '|ANSI bright white color|',
};

function buildScript(variants) {
  const blocks = Object.values(variants).map(v => {
    const name = v.label === 'Tinacious Design' ? 'Tinacious Design Dark' : v.label;
    const t = v.terminal;
    const ui = v.ui;

    const ansiLines = [];
    for (const { key, normal, bright } of ansiPairs(t)) {
      ansiLines.push(`        set ${ANSI_PROP[key]} to ${hexToAppleScript(normal)}`);
      ansiLines.push(`        set ${ANSI_BRIGHT_PROP[key]} to ${hexToAppleScript(bright)}`);
    }

    return `
    -- ${name}
    try
      delete settings set "${name}"
    end try
    set s to make new settings set with properties {name: "${name}"}
    tell s
        set background color to ${hexToAppleScript(t.background)}
        set normal text color to ${hexToAppleScript(t.foreground)}
        set bold text color to ${hexToAppleScript(t.foreground)}
        set cursor color to ${hexToAppleScript(ui.cursor)}
        set font name to "OperatorMono-Book"
        set font size to 16
${ansiLines.join('\n')}
    end tell`;
  });

  return `-- Generated by build-terminal.mjs — do not edit by hand
-- Run with: osascript setup-profile.applescript
tell application "Terminal"
${blocks.join('\n')}
end tell
`;
}

function main() {
  const { variants } = loadPalette();
  writeDistFile('terminal', 'setup-profile.applescript', buildScript(variants));
  console.log('Wrote dist/terminal/setup-profile.applescript from palette/tokens.json');
}

main();
